    private void jButtonConfirmaçaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmaçaoActionPerformed
        jLabelOutput.setText("");
        int resposta = JOptionPane.showConfirmDialog(this,
                "Deseja continuar ?", "Confirmação",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);
        if (resposta == JOptionPane.NO_OPTION) {
            jLabelOutput.setText("Click no botão NO");
        } else if (resposta == JOptionPane.YES_OPTION) {
            jLabelOutput.setText("Click no botão Yes");
        } else if (resposta == JOptionPane.CLOSED_OPTION) {
            jLabelOutput.setText("JOptionPane closed");
        }
    }//GEN-LAST:event_jButtonConfirmaçaoActionPerformed

