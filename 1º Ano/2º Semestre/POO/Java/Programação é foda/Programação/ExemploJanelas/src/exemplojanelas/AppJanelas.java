    private void jButtonEscolhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEscolhaActionPerformed
        jLabelOutputEscolha.setText("");
        Object[] selectionValues = {"Vermelho", "Verde", "Azul",
            "Amarelo"};
        String initialSelection = "Vermelho";
        Object selection = JOptionPane.showInputDialog(null,
                "Qual a tua cor favorita?",
                "Quiz de cores",
                JOptionPane.QUESTION_MESSAGE, null,
                selectionValues, initialSelection);
        if (selection != null) {
            jLabelOutputEscolha.setText(selection.toString());
        }
    }//GEN-LAST:event_jButtonEscolhaActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jLabelOutputBotoes.setText("");
        Object stringArray[] = {"Sim", "Não", "Talvez"};
        int resposta = JOptionPane.showOptionDialog(this,
                "Continuar a operação?", "Escolha a opção?",
                JOptionPane.YES_NO_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE, null, stringArray,
                stringArray[0]);
        if (resposta == JOptionPane.NO_OPTION) {
            jLabelOutputBotoes.setText("Click no botão Não");
        } else if (resposta == JOptionPane.YES_OPTION) {
            jLabelOutputBotoes.setText("Click no botão Sim");
        } else if (resposta == JOptionPane.CANCEL_OPTION) {
            jLabelOutputBotoes.setText("Click no botão Talvez");
        } else if (resposta == JOptionPane.CLOSED_OPTION) {
    }//GEN-LAST:event_jButton1ActionPerformed
    }

